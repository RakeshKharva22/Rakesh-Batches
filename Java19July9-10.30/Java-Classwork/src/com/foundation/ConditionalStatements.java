/*
 * Conditional statements
 * 
 * 1) Simple If.
 * 2) if/else.
 * 3) nested if/else.
 * 4) ladder if/else.
 * 5) switch case. 
 * 
 * 
 * 1) Simple If : 
 * 
 * Syntax:
 * 
 * 		if(condition)
 * 		{
 * 			statement;
 * 		}
 * 
 * 2) if/else
 * 
 * 	Syntax :
 * 
 * 		if(condition)
 * 		{
 * 			statement;
 * 		}
 * 		else
 * 		{
 * 			statement
 * 		}
 * 
 * 3) Nested if/else
 * 
 * 
 * 	Syntax : 
 * 
 * 		if(condition)
 * 		{
 * 			if(condition)
 * 			{
 * 				statement;
 * 			}
 * 			else
 * 			{
 * 				statement;
 * 			}
 * 		}
 * 		else
 * 		{
 * 			if(condition)
 * 			{
 * 				condition
 * 			}
 * 			else
 * 			{
 * 				condition
 * 			}
 * 		}
 * 
 *  4) Ladder if/else
 *  
 *  Syntax:
 *  
 *  	if(condition)
 *  	{
 *  		statement;
 *  	}
 *  	else if(condition)
 *  	{
 *  		statement;
 *  	}
 *  	else if(condition)
 *  	{
 *  		statement;
 *  	}
 *  	else if(condition)
 *  	{
 *  		statement;
 *  	}
 *  	else
 *  	{
 *  		statement;
 *  	}
 *  
 *  5) Switch(condition)
 *  
 *  	Syntax :
 *  
 *  	switch(condition)
 *  	{
 *  		case literal: statement;
 *  					  break;
 *  		case literal: statement;
 *  					  break;
 *  		case literal: statement;
 *  					  break;
 *  		case literal: statement;
 *  					  break;
 *  		default 	: statement;
 *  					  break;
 *  	
 *  	}
 * 
 */


